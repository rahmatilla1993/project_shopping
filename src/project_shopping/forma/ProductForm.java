    private void browse_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browse_btnActionPerformed
        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter(".Images","jpg","png","gif","jpeg");
        file.addChoosableFileFilter(filter);
        int res = file.showSaveDialog(file);
        if(res == JFileChooser.APPROVE_OPTION){
            isAdded = true;
            File selectedFile = file.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            String destinationPath = destination;
            String format = path.substring(path.lastIndexOf(".") + 1);
            String fileName = UUID.randomUUID().toString() + "." + format;
            destinationPath += fileName;
            pathIn = Paths.get(path);
            pathout = Paths.get(destinationPath);
            place_photo.setIcon(ReSizeCover(path));
            img_path = destinationPath;
        }
        else if(res == JFileChooser.CANCEL_OPTION){
            JOptionPane.showMessageDialog(this, "No file selected");
        }
    }//GEN-LAST:event_browse_btnActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        TableModel model = jTable1.getModel();
        String id = model.getValueAt(jTable1.getSelectedRow(), 0).toString();
        String name = model.getValueAt(jTable1.getSelectedRow(), 1).toString();
        String delivery = model.getValueAt(jTable1.getSelectedRow(), 2).toString();
        String validate = model.getValueAt(jTable1.getSelectedRow(), 3).toString();
        String status = model.getValueAt(jTable1.getSelectedRow(), 4).toString();
        String price = model.getValueAt(jTable1.getSelectedRow(), 5).toString();
        String photo = model.getValueAt(jTable1.getSelectedRow(), 6).toString();
        String id_cat = model.getValueAt(jTable1.getSelectedRow(), 7).toString();
        String id_brend = model.getValueAt(jTable1.getSelectedRow(), 8).toString();
        
        img_path = photo;
        id_product = Integer.parseInt(id);
        name_field.setText(name);
        java.util.Date delivery_date = null;
        try {
            delivery_date = new SimpleDateFormat("yyyy-MM-dd").parse(delivery);
        } catch (ParseException ex) {
            Logger.getLogger(ProductForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        date_delivery.setDate(delivery_date);
        
        java.util.Date validate_date = null;
        try {
            validate_date = new SimpleDateFormat("yyyy-MM-dd").parse(validate);
        } catch (ParseException ex) {
            Logger.getLogger(ProductForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        date_validate.setDate(validate_date);
        if(status.equals("true")){
            status_field.setSelectedItem("Active");
        }
        else{
            status_field.setSelectedItem("Not Active");
        }
        price_field.setText(price);
        place_photo.setIcon(ReSizeCover(photo));
        category_field.setSelectedItem(cat_service.getCategoryNameById(Integer.parseInt(id_cat)));
        brend_field.setSelectedItem(brend_service.getBrandNameById(Integer.parseInt(id_brend)));
        
    }//GEN-LAST:event_jTable1MouseClicked

