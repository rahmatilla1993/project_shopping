    private boolean isAdded = false;
    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed

        // TODO add your handling code here:
        int res = JOptionPane.showConfirmDialog(this, "Are you sure want insert data?");
        if(res == JOptionPane.YES_OPTION){
            String name = name_field.getText();
            Date delivery_date = new java.sql.Date(date_delivery.getDate().getTime());
            Date validate_date = new java.sql.Date(date_validate.getDate().getTime());
            Integer cat_id = cat_service.getCategoryIdByName(category_field.getSelectedItem().toString());
            Integer brend_id = brend_service.getBrandIdByName(brend_field.getSelectedItem().toString());
            Float price = Float.parseFloat(price_field.getText());
            Boolean status = false;
            if(status_field.getSelectedItem().toString().equals("Active")){
                status = true;
            }
            Product product = new Product(name,delivery_date,validate_date,status,price,img_path,cat_id,brend_id);
            if(prod_service.addProduct(product)){
                try {
                    Files.copy(pathIn, pathout, StandardCopyOption.REPLACE_EXISTING);
                    JOptionPane.showMessageDialog(this, "Product successfully added!");
                    jTable1.setModel(DbUtils.resultSetToTableModel(prod_service.getProducts()));
                } catch (IOException ex) {
                    Logger.getLogger(ProductForm.class.getName()).log(Level.SEVERE, null, ex);
                }
                isAdded = false;
            }
            else{
                JOptionPane.showMessageDialog(this, "Xatolik");
            }
        }
        else if(res == JOptionPane.CANCEL_OPTION){
            JOptionPane.showMessageDialog(this, "No File selected");
        }
    }//GEN-LAST:event_addBtnActionPerformed
    
